package com.csse3200.game.data.credits;

public class Star {
  private float x;
  private float y;
  private float z;
  private float size;

  public float getX() {
    return x;
  }

  public void setX(float x) {
    this.x = x;
  }

  public float getY() {
    return y;
  }

  public void setY(float y) {
    this.y = y;
  }

  public float getZ() {
    return z;
  }

  public void setZ(float z) {
    this.z = z;
  }

  public float getSize() {
    return size;
  }

  public void setSize(float size) {
    this.size = size;
  }
}
